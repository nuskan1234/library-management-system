    private void incrementBookID() {
        try {
            //Connection connection = DriverManager.getConnection("jdbc:mysql://localhost/library_management", "root", "");
            Connection con=DatabaseCon.createDBConnection();
            // Query the database to get the next book ID
            String query = "SELECT MAX(student_id) + 1 FROM student";
            PreparedStatement preparedStatement = con.prepareStatement(query);
            ResultSet resultSet = preparedStatement.executeQuery();

            if (resultSet.next()) {
                int nextstudentId = resultSet.getInt(1);
                //nextBookId++;
                lbl_studentid.setText(Integer.toString(nextstudentId));
            }

            con.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
        
    }
    public boolean addStudent(){
        boolean isAdded=false;
        //book_id=Integer.parseInt(lbl_bid.getText());
        name=txt_sname.getText();
        fname=txt_f_name.getText();
        course=cmb_course.getSelectedItem().toString();
        branch=cmb_branch.getSelectedItem().toString();
        year=cmb_year.getSelectedItem().toString();
        sem=cmb_sem.getSelectedItem().toString();
        
        try {
            Connection con=DatabaseCon.createDBConnection();
            //String quary="insert into book values(?,?,?,?,?,?)";
            //PreparedStatement pst=con.prepareStatement(quary);
            String query = "INSERT INTO student (name,fathers_name,course,branch,year,semister) VALUES (?, ?, ?, ?, ?,?)";
            PreparedStatement pst = con.prepareStatement(query, Statement.RETURN_GENERATED_KEYS);
            //pst.setInt(1, book_id);
            pst.setString(1, name);
            pst.setString(2, fname);
            pst.setString(3, course);
            pst.setString(4, branch);
            pst.setString(5, year);
            pst.setString(6, sem);
            int rowCount=pst.executeUpdate();
            if(rowCount>0){
                ResultSet generatedKeys = pst.getGeneratedKeys();
            if (generatedKeys.next()) {
                studentid = generatedKeys.getInt(1);
                lbl_studentid.setText(Integer.toString(studentid)); // Update the label with the generated book ID
            }
                isAdded=true;
            }
            else{
                isAdded=false;
            }
                
        } catch (Exception e) {
            e.printStackTrace();
        }
        return isAdded;
    }

