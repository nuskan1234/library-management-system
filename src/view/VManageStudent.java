    public void setStudenttable(){
        try {
            String path = "jdbc:mysql://localhost/library_management";
            con=DriverManager.getConnection(path, "root", "");
            //Statement st= DatabaseCon.createDBConnection().createStatement();
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select*from student");
            
            while (rs.next()) {
                int studentid = rs.getInt("student_id");
                String name=rs.getString("name");
                String fname=rs.getString("fathers_name");
                String course=rs.getString("course");
                String branch=rs.getString("branch");
                String year=rs.getString("year");
                String sem=rs.getString("semister");
                
                Object[] obj ={studentid,name,fname,course,branch,year,sem};
                model=(DefaultTableModel)table_manage_Student.getModel();
                model.addRow(obj);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
